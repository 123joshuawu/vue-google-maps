<template>
  <div>
    <div ref="flyaway"> <!-- so named because it will fly away to another component -->
      <slot>
        <div v-html="content"></div>
      </slot>
    </div>
  </div>
</template>

<script>

import _ from 'lodash'
import propsBinder from '../utils/propsBinder.js'
import eventsBinder from '../utils/eventsBinder.js'
import MapComponent from './mapComponent';
import Marker from './marker.vue';

const props = {
  options: {
    type: Object,
    required: false,
    default () {
      return {};
    }
  },
  content: {
    default: null
  },
  opened: {
    type: Boolean,
    default: true,
  },
  position: {
    type: Object,
    twoWay: true,
  },
  zIndex: {
    type: Number,
    twoWay: true,
  }
}

const events = [
  'domready',
  'closeclick',
  'content_changed',
]

export default MapComponent.extend({
  replace: false,
  props: props,

  created() {
    this.$markerComponent = this.$findAncestor(
      (ans) => ans instanceof Marker
    )
  },

  mounted() {
    const el = this.$refs.flyaway;
    el.parentNode.removeChild(el);
  },

  deferredReady() {
    if (this.$markerComponent) {
      this.$markerObject = this.$markerComponent.$markerObject;
    }
    this.createInfoWindow(this.$map);
  },

  destroyed () {
    if (this.disconnect) {
      this.disconnect();
    }
    if (this.$infoWindow) {
      this.$infoWindow.setMap(null);
    }
  },

  methods: {
    openInfoWindow () {
        if(this.opened) {
          if (this.$markerObject !== null) {
            this.$infoWindow.open(this.$map, this.$markerObject);
          } else {
            this.$infoWindow.open(this.$map);
          }
        } else {
          this.$infoWindow.close();
        }
    },

    createInfoWindow(map) {
      // setting options
      const options = _.clone(this.options);
      options.content = this.$refs.flyaway;

      // only set the position if the info window is not bound to a marker
      if (this.$markerObject === null) {
        options.position = this.position;
      }

      this.$infoWindow = new google.maps.InfoWindow(options);

      // Binding
      const propsToBind = _.clone(props);
      delete propsToBind.opened;
      propsBinder(this, this.$infoWindow, propsToBind);
      eventsBinder(this, this.$infoWindow, events);

      this.$watch('opened', () => {
        this.openInfoWindow();
      });

      // Open if needed
      this.openInfoWindow();
    }
  },
})

</script>
